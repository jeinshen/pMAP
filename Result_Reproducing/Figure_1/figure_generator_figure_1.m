function figure_generator_figure_1(infos_result)
%% Method to plot Figure 1 using generated kpi data (infos_result)

fprintf('\n-------------------------------------------------');
fprintf('\n Step 4/4: generate and save figures for Figure 1');

infos_w1 = infos_result{1};
infos_w2 = infos_result{2};

f = figure('visible','off');
pfhist_w1 = infos_w1.pfhist;
it_w1 = infos_w1.it - 1;
T_w1 = (1 : it_w1)';
S_w1 = line(T_w1, log(pfhist_w1(1:it_w1, 1))); 
hold on

pfhist_w2 = infos_w2.pfhist;
it_w2 = infos_w2.it - 1;
T_w2 = (1 : it_w2)';
S_w2 = line(T_w2, log(pfhist_w2(1:it_w2, 1))); 
hold on

figure_font_size = 24;

set(S_w1, ...                        
    'LineStyle',    '-'   , ...
    'LineWidth',    1.5,  ...
    'color'    ,  [1 0 1],  ...
    'Marker'   , 'o'      , ...
    'MarkerSize'  , 7 , ...
    'MarkerEdgeColor', [1 0 1], ...
    'MarkerFaceColor', [0 1 0]);

set(S_w2, ...                        
    'LineStyle',    '-.'   , ...
    'LineWidth',    1.5,  ...
    'color'    ,  [0 0.5 1],  ...
    'Marker'   , 'x'      , ...
    'MarkerSize'  , 10 , ...
    'MarkerEdgeColor', [0 0.5 1], ...
    'MarkerFaceColor', [0 0.5 1]);


set(gca,'FontSize',figure_font_size);
set(gca,'XTick',0:5:200);
ylabel('$log(F_{\rho}(X^{\nu}))$', 'Interpreter', 'latex');

xlabel('Iteration Steps');
title('n = 2000, r = 20, \theta = 0.2');
set(gcf,'Position',[100 100 800 600]);
SLegend = legend( ...
    [S_w1, S_w2], ...
    '$ W = W_1 $', '$W = W_2$', ...
    'location', 'Best');
set(SLegend,'Interpreter','latex','FontSize', figure_font_size);

saveas(f,'Output/Fig_1(a).eps','epsc')


f = figure('visible','off');
xist_w1 = infos_w1.Xhist;
T_w1 = (1 : it_w1)';
S_w1 = line(T_w1, log(xist_w1(1:it_w1, 1))); 
hold on

xhist_w2 = infos_w2.Xhist;
T_w2 = (1 : it_w2)';
S_w2 = line(T_w2, log(xhist_w2(1:it_w2, 1))); 

set(S_w1, ...                        
    'LineStyle',    '-'   , ...
    'LineWidth',    1.5,  ...
    'color'    ,  [0 0 1],  ...
    'Marker'   , 's'      , ...
    'MarkerSize'  , 7 , ...
    'MarkerEdgeColor', [0 0 1], ...
    'MarkerFaceColor', [0 1 0]);

set(S_w2, ...                        
    'LineStyle',    '-.'   , ...
    'LineWidth',    1.5,  ...
    'color'    ,  [1 0 0],  ...
    'Marker'   , 'v'      , ...
    'MarkerSize'  , 7 , ...
    'MarkerEdgeColor', [1 0 1], ...
    'MarkerFaceColor', [1 0 1]);


set(gca,'FontSize',figure_font_size);
set(gca,'XTick',0:5:200);
title('n = 2000, r = 20, \theta = 0.2');
ylabel('$log(\|X^{\nu+1} - X^{\nu}\|)$', 'Interpreter', 'latex');
xlabel('Iteration Steps');
set(gcf,'Position',[100 100 800 600]);
SLegend = legend( ...
    [S_w1, S_w2], ...
    '$W = W_1$','$W = W_2$', ...
    'location', 'Best');
set(SLegend,'Interpreter','latex','FontSize', figure_font_size);

saveas(f,'Output/Fig_1(b).eps','epsc')

fprintf('\n-------------------------------------------------');
fprintf('\n Figure 1 succesfully generated. Please find the Fig_1(a).eps and Fig_1(b).eps in /Output folder');
fprintf('\n-------------------------------------------------\n');

end

